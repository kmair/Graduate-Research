      SUBROUTINE CNVBRD(N,A,B)
C
C     CNVBRD CONVERTS THE N REAL ITEMS IN A
C     TO DOUBLE PRECISION ITEMS PUTTING THE RESULT IN B.
C     A BACKWARDS DO LOOP IS USED.
C
      REAL A(1)
      DOUBLE PRECISION B(1)
C
      I = N
C
 10   IF(I .LE. 0) RETURN
        B(I) = A(I)
        I = I - 1
        GO TO 10
C
      END
