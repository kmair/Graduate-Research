      SUBROUTINE CNVFDI(N,A,B)
C
C     CNVFDI CONVERTS THE N DOUBLE PRECISION ITEMS IN A
C     TO INTEGER ITEMS PUTTING THE RESULT IN B.
C     A FOWARDS DO LOOP IS USED.
C
      DOUBLE PRECISION A(1)
      INTEGER B(1)
C
      IF(N .LE. 0) RETURN
C
      DO 10 I = 1, N
 10     B(I) = A(I)
C
      RETURN
C
      END
